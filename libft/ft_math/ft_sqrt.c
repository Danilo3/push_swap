unsigned	ft_sqrt(unsigned x)
{
	unsigned m;
	unsigned y;
	unsigned b;

	m = 0x40000000;
	y = 0;
	while (m != 0)
	{
		b = y | m;
		y >>= 1;
		if (x >= b)
		{
			x -= b;
			y |= m;
		}
		m >>= 2;
	}
	return (y);
}
