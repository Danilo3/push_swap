#include "libft.h"

int		ft_int_arr_cmp(int *a1, int *a2, size_t size)
{
	size_t i;

	i = 0;
	while (i < size)
	{
		if (a1[i] != a2[i])
			return (0);
		i++;
	}
	return (1);
}
