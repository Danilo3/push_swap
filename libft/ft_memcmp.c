#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	t_uchar	*p1;
	t_uchar	*p2;

	i = 0l;
	p1 = (t_uchar*)s1;
	p2 = (t_uchar*)s2;
	while (i < n)
	{
		if (p1[i] != p2[i])
			return (p1[i] - p2[i]);
		i++;
	}
	return (0);
}
